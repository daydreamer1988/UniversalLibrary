    /**
     * 获取与当前时间的差
     *
     * @param data   传入的时间
     * @param format 传入时间的格式
     * @return
     */
    public static String getMistiming(String data, String format) {
        long time = getCurrentTiems(data, format);
        long curtime = System.currentTimeMillis();
        long l = curtime - time;//据当前时间的差值
        long minter = 60 * 1000;//1分钟
        long hour = 60 * minter;//1小时
        long day = 24 * hour;//1天
        long week = 7 * day;//1周
        long monuth = 30 * day;//1月
        long years = 52 * week;
        if (l < minter) {
            return "刚刚";
        } else if (l >= minter && l < hour) {
            return l / minter + "分钟前";
        } else if (l >= hour && l < day) {
            return l / hour + "小时前";
        } else if (l >= day && l < week) {
            return l / day + "天前";
        } else if (l >= week && l < monuth) {
            return l / week + "周前";
        } else if (l >= monuth && l < years) {
            return l / monuth + "月前";
        } else {
            return l / years + "年前";
        }
    }

    /**
     * 二个小时时间间的差值,必须保证二个时间都是"HH:MM"的格式，返回字符型的分钟
     */
    public static String getTwoHour(String st1, String st2) {
        String[] kk = null;
        String[] jj = null;
        kk = st1.split(":");
        jj = st2.split(":");
        if (Integer.parseInt(kk[0]) < Integer.parseInt(jj[0]))
            return "0";
        else {
            double y = Double.parseDouble(kk[0]) + Double.parseDouble(kk[1]) / 60;
            double u = Double.parseDouble(jj[0]) + Double.parseDouble(jj[1]) / 60;
            if ((y - u) > 0)
                return y - u + "";
            else
                return "0";
        }
    }


